			<p>Counseling and support groups assist newcomer and immigrant women in dealing with issues of isolation from their extended families who remain in the home country, family problems related to adaptation to a new culture and family re-unification, domestic and family violence, unfamiliarity with Canadian institutions including schools, the legal system, health care as well as barriers to participating in civic activities.</p>
			<p>Counselors work closely with women with a variety of techniques and provide responsive,culturally-sensitive and effective supportive counseling to help resolve conflicts, and problems with the focus on setting goals and problem solving. We provide short term action oriented counseling for women who are struggling with abuse. Counselors also run support groups to address a variety of issues, especially settlement and abuse. These support groups enable women to develop leadership skills through self-development and empowerment.</p>
			<p>WWCC offers the following support groups for immigrant women:</p>
			<p>Cook & Talk – This long-running group is held two times a year, with each session running for 12 weeks. Childcare is provided. Through cooking and talking, women have the opportunity to discuss various topics, make friends and break isolation.</p>
			<p>Portuguese Women in Transition – This group runs for 12 weeks on a weekly basis for women who are experiencing or trying to escape abusive relationships.</p>
			<p>Querer e Poder – Running twice a week for 15 weeks, this group focusses on leadership development. Participants are trained to provide court peer support and escort for other women going through the court process.</p>
			<p>Educar Educande se – This 10 week program is a self-help open group focused on reaching out to Brazilian women. The group focuses on enhancing self-esteem and self-awareness.</p>
			<p>Portuguese Seniors Group, Farsi Speaking Seniors Group, Chinese Seniors Group – With support from volunteers, these groups invite guest speakers and meet to socialize and network with other older women. Fitness is an important component of these groups.</p>
